package corejava.concepts.operators;

public class LearningUnaryOperators
{
	
	public static void main(String[] args)
	{
		
		int a=5;
		int b=6;
		boolean condition=true;
				
		System.out.println("Value of pre increment operator ++a=" +(++a));
		System.out.println("Value of post decrement operator a--=" +(a--));
		System.out.println("Value of logical not operator!condition=" +(!condition));
		
	}
	
}
