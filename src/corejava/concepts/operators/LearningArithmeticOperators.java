package corejava.concepts.operators;

public class LearningArithmeticOperators
{
	public static void main(String[] args)
	{
		int a=5;
		int b=6;
		
		String x1="Biswajit";
		String y1="Sundara";
		
		
		System.out.println("Addition operator for string=" +(x1+y1));
		System.out.println("The addition a+b=" +(a+b));
		System.out.println("The subtraction a-b=" +(a-b));
		System.out.println("The multiplication a*b=" +(a*b));
		System.out.println("The division a/b=" +(a/b));
		System.out.println("The modulo a%b=" +(a%b));
		

	}

}