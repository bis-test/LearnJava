package corejava.concepts.operators;

public class LearningAssignmentOperator
{
	
	public static void main(String[] args)
	{
		
		int a=5;
		int b=6;
		
		int c=a+b;
		int d = 5;
		d += 5;
		d -=5;
		d *=5;
		
		
	
		
		
		System.out.println("The value of (c=a+b)=" +(c));
		System.out.println("The value of  (d+=5)=" +(d));
		System.out.println("The value of  (d-=5)=" +(d));
		System.out.println("The value of  (d*=5)=" +(d));
	}
}