package corejava.concepts.operators;

public class LearningLogicalOperators {
	
	public static void main(String[] args)
	{
		
	
	int a=5;
	int b=6;
	
	if (a==5 && b==6) 
	{
		System.out.println("a is smaller than b");
	
	}

	else if (a==5 || b==8) 
	{
		System.out.println("a is greater than b");
	
	}
	
	}
}
